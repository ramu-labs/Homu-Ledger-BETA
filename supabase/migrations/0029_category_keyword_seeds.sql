-- v1.44.0 — Global category keyword seed table.
--
-- Replaces the per-household keyword seeding from migration 0023, which
-- (a) used stale category names from before the v1.40.1 category
-- redesign and (b) inserted a copy of every seed row into every
-- household — wasteful as the user base grows.
--
-- This table is GLOBAL: one shared set of ~2800 keyword→category-name
-- mappings, read by suggestCategory() on a cache miss. The per-household
-- category_hints table stays, but now holds only AI-learned and
-- user-corrected mappings (source in 'ai','user') — not 'seed'.
--
-- Lookup order in suggestCategory():
--   1. disambiguation rules (lib/llm/disambiguation.ts) — regex forcing
--   2. category_hints  (this household's learned + corrected mappings)
--   3. category_keyword_seeds  (this global table)  ← NEW
--   4. Gemini AI fallback
--
-- Data sourced from intensive EN/ID keyword + brand research. Keywords
-- are lowercased, apostrophe-stripped, deduped so each maps to exactly
-- one category. category_name matches the v1.40.1 default category set.

-- ── Table ─────────────────────────────────────────────────────────────
create table if not exists public.category_keyword_seeds (
  keyword       text primary key,
  category_name text not null,
  -- 'en' | 'id' | 'xx' (brand or language-neutral)
  lang          text not null default 'xx',
  -- 'term' (generic word) | 'brand' (store / product / chain name)
  kind          text not null default 'term' check (kind in ('term','brand')),
  created_at    timestamptz not null default now()
);

create index if not exists category_keyword_seeds_category_idx
  on public.category_keyword_seeds (category_name);

-- Global read-only reference data. Any signed-in user may read it; no
-- one writes via the API (only migrations). Enabling RLS with a single
-- permissive SELECT keeps the security advisor quiet without exposing
-- a write path.
alter table public.category_keyword_seeds enable row level security;

drop policy if exists "category_keyword_seeds: authenticated read" on public.category_keyword_seeds;
create policy "category_keyword_seeds: authenticated read"
  on public.category_keyword_seeds for select
  to authenticated
  using (true);

